// Повторять цикл, пока ввод неверен
let num;

do {
  num = prompt("Напишите число больше 100");
} while (num <= 100);

// Вывести простые числа
let num1 = 10;

for (let i = 2; i <= num1; i++) {
  for (let j = 2; j < i; j++) {
    if (i % j == 0) continue;
  }

  alert(i);
}